static inline void xmss_write32(uint32_t offset, uint32_t value) {
    volatile uint32_t *ptr = (volatile uint32_t *)(XMSS_PERIPH_START_ADDRESS + offset);
    *ptr = value;
}

static inline uint32_t xmss_read32(uint32_t offset) {
    volatile uint32_t *ptr = (volatile uint32_t *)(XMSS_PERIPH_START_ADDRESS + offset);
    return *ptr;
}

// Escribe un fragmento en la BRAM (el offset es el INDICE * 32 bytes)
static void load_to_bram_idx(uint32_t idx, const uint8_t *src, uint32_t len) {
    uint32_t words = (len + 3) / 4;
    uint32_t blocks = (words + 7) / 8; 
    uint32_t total_words = blocks * 8;
    uint32_t base_addr = XMSS_BRAM_OFFSET + (idx * 32);

    for (uint32_t i = 0; i < total_words; i++) {
        uint32_t word = 0;
        if (i * 4 + 0 < len) word |= ((uint32_t)src[i*4 + 0]) << 0;
        if (i * 4 + 1 < len) word |= ((uint32_t)src[i*4 + 1]) << 8;
        if (i * 4 + 2 < len) word |= ((uint32_t)src[i*4 + 2]) << 16;
        if (i * 4 + 3 < len) word |= ((uint32_t)src[i*4 + 3]) << 24;
        xmss_write32(base_addr + (i * 4), word);
    }
}

