    uint32_t msg_len_bits = msg_len_bytes * 8;
    printf("[XMSS_HW] Configurando MLEN = %lu bits\n", (unsigned long)msg_len_bits);
    xmss_write32(XMSS_MLEN_OFFSET, msg_len_bits);

    printf("[XMSS_HW] Disparando el acelerador (ENABLE = 1)...\n");
    xmss_write32(XMSS_CTRL_OFFSET, 1u);

