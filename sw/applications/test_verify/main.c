    // ============================================================================
    uint32_t status;
    uint32_t timeout_counter = 0;
    
    do {
        status = xmss_read32(XMSS_STATUS_OFFSET);
        timeout_counter++;
        if (timeout_counter > 5000000) {
            printf("[FAIL] TIMEOUT: El hardware no contesto.\n");
            xmss_write32(XMSS_CTRL_OFFSET, 0u); 
            return -1;
        }
    } while (((status >> 16) & 0x1u) == 0); 

    xmss_write32(XMSS_CTRL_OFFSET, 0u);

    uint16_t valid_flag = (uint16_t)(status & 0xFFFFu);
    
    printf("[XMSS_HW] Hardware Finalizado. STATUS = 0x%08lx\n", (unsigned long)status);

